import 'dart:convert';

class ForecastWeatherResponse {
  String? cod;
  int? message;
  int? cnt;
  List<ListElement>? list;
  City? city;

  ForecastWeatherResponse({
    this.cod,
    this.message,
    this.cnt,
    this.list,
    this.city,
  });

  ForecastWeatherResponse copyWith({
    String? cod,
    int? message,
    int? cnt,
    List<ListElement>? list,
    City? city,
  }) =>
      ForecastWeatherResponse(
        cod: cod ?? this.cod,
        message: message ?? this.message,
        cnt: cnt ?? this.cnt,
        list: list ?? this.list,
        city: city ?? this.city,
      );

  factory ForecastWeatherResponse.fromRawJson(String str) =>
      ForecastWeatherResponse.fromJson(json.decode(str));

  String toRawJson() => json.encode(toJson());

  factory ForecastWeatherResponse.fromJson(Map<String, dynamic> json) =>
      ForecastWeatherResponse(
        cod: json["cod"],
        message: json["message"],
        cnt: json["cnt"],
        list: json["list"] == null
            ? []
            : List<ListElement>.from(
                json["list"]!.map((x) => ListElement.fromJson(x))),
        city: json["city"] == null ? null : City.fromJson(json["city"]),
      );

  Map<String, dynamic> toJson() => {
        "cod": cod,
        "message": message,
        "cnt": cnt,
        "list": list == null
            ? []
            : List<dynamic>.from(list!.map((x) => x.toJson())),
        "city": city?.toJson(),
      };
}

